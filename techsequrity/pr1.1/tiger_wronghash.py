def tiger_round(a, b, c, x, mul):
    c ^= x
    a -= (T1[c & 0xFF] ^
          T2[(c >> 16) & 0xFF] ^
          T3[(c >> 32) & 0xFF] ^
          T4[(c >> 48) & 0xFF])
    b += (T4[(c >> 8) & 0xFF] ^
          T3[(c >> 24) & 0xFF] ^
          T2[(c >> 40) & 0xFF] ^
          T1[(c >> 56) & 0xFF])
    b *= mul
    return a & 0xFFFFFFFFFFFFFFFF, b & 0xFFFFFFFFFFFFFFFF, c & 0xFFFFFFFFFFFFFFFF

def tiger_pass(a, b, c, x, mul):
    for i in range(8):
        a, b, c = tiger_round(a, b, c, x[i], mul)
        a, b, c = b, c, a
    return a, b, c

def tiger(message: bytes) -> str:
    a = 0x0123456789ABCDEF
    b = 0xFEDCBA9876543210
    c = 0xF096A5B4C3B2E187
    length = len(message)
    message += b'\x01'
    while len(message) % 64 != 56:
        message += b'\x00'
    message += struct.pack('<Q', length * 8)
    for i in range(0, len(message), 64):
        block = message[i:i+64]
        x = list(struct.unpack('<8Q', block))
        aa, bb, cc = a, b, c
        a, b, c = tiger_pass(a, b, c, x, 5)
        a, b, c = tiger_pass(a, b, c, x, 7)
        a, b, c = tiger_pass(a, b, c, x, 9)
        a ^= aa
        b -= bb
        c += cc
        a &= 0xFFFFFFFFFFFFFFFF
        b &= 0xFFFFFFFFFFFFFFFF
        c &= 0xFFFFFFFFFFFFFFFF
    return ''.join(f'{x:016x}' for x in [a, b, c])

def tiger_file(path: str) -> str:
    with open(path, 'rb') as f:
        return tiger(f.read())

